//! Format exporters for CrystalCanvas - Strong-typed Writer Implementations
use crate::crystal_state::CrystalState;
use std::collections::HashMap;
use std::fs::File;
use std::io::{self, Write};

/// Abstract structure representing a VASP POSCAR file
pub struct PoscarFile {
    pub comment: String,
    pub scaling_factor: f64,
    pub lattice: [[f64; 3]; 3],
    pub elements: Vec<String>,
    pub element_counts: Vec<usize>,
    pub coordinates: Vec<[f64; 3]>,
}

impl PoscarFile {
    pub fn from_state(state: &CrystalState) -> Self {
        let alpha = state.cell_alpha.to_radians();
        let beta = state.cell_beta.to_radians();
        let gamma = state.cell_gamma.to_radians();
        let c = state.cell_c;

        let cx = c * beta.cos();
        let cy = c * (alpha.cos() - beta.cos() * gamma.cos()) / gamma.sin();
        let cz = (c * c - cx * cx - cy * cy).sqrt();

        let lattice = [
            [state.cell_a, 0.0, 0.0],
            [state.cell_b * gamma.cos(), state.cell_b * gamma.sin(), 0.0],
            [cx, cy, cz],
        ];

        let mut ordered_elements = Vec::new();
        let mut atom_groups: HashMap<String, Vec<[f64; 3]>> = HashMap::new();

        for i in 0..state.num_atoms() {
            let elem = &state.elements[i];
            if !atom_groups.contains_key(elem) {
                ordered_elements.push(elem.clone());
                atom_groups.insert(elem.clone(), Vec::new());
            }
            atom_groups.get_mut(elem).unwrap().push([
                state.fract_x[i],
                state.fract_y[i],
                state.fract_z[i],
            ]);
        }

        let mut counts = Vec::new();
        let mut coordinates = Vec::new();

        for elem in &ordered_elements {
            let group = atom_groups.get(elem).unwrap();
            counts.push(group.len());
            for pos in group {
                coordinates.push(*pos);
            }
        }

        Self {
            comment: format!("{} generated by CrystalCanvas", state.name),
            scaling_factor: 1.0,
            lattice,
            elements: ordered_elements,
            element_counts: counts,
            coordinates,
        }
    }

    pub fn write_to<W: Write>(&self, writer: &mut W) -> io::Result<()> {
        writeln!(writer, "{}", self.comment)?;
        writeln!(writer, "{:.6}", self.scaling_factor)?;
        for row in &self.lattice {
            writeln!(writer, "  {:.6} {:.6} {:.6}", row[0], row[1], row[2])?;
        }
        for elem in &self.elements {
            write!(writer, "{} ", elem)?;
        }
        writeln!(writer)?;
        for count in &self.element_counts {
            write!(writer, "{} ", count)?;
        }
        writeln!(writer)?;
        writeln!(writer, "Direct")?;
        for pos in &self.coordinates {
            writeln!(writer, "  {:.6} {:.6} {:.6}", pos[0], pos[1], pos[2])?;
        }
        Ok(())
    }
}

pub fn export_poscar(state: &CrystalState, path: &str) -> io::Result<()> {
    let mut file = File::create(path)?;
    let poscar = PoscarFile::from_state(state);
    poscar.write_to(&mut file)
}

/// Abstract structure representing a LAMMPS Data file
pub struct LammpsDataFile {
    pub comment: String,
    pub num_atoms: usize,
    pub atom_types: usize,
    pub box_bounds: [([f64; 2], String); 2], // [(min, max), axis] limited to x,y,z and xy,xz,yz
    pub box_x: [f64; 2],
    pub box_y: [f64; 2],
    pub box_z: [f64; 2],
    pub tilts: [f64; 3], // xy, xz, yz
    pub masses: Vec<(usize, f64, String)>,
    pub atoms: Vec<(usize, usize, [f64; 3])>, // id, type, coords
}

impl LammpsDataFile {
    pub fn from_state(state: &CrystalState) -> Self {
        let alpha = state.cell_alpha.to_radians();
        let beta = state.cell_beta.to_radians();
        let gamma = state.cell_gamma.to_radians();
        let b = state.cell_b;
        let c = state.cell_c;

        let lx = state.cell_a;
        let xy = b * gamma.cos();
        let xz = c * beta.cos();
        let ly = (b * b - xy * xy).sqrt();
        let yz = (b * c * alpha.cos() - xy * xz) / ly;
        let lz = (c * c - xz * xz - yz * yz).sqrt();

        let mut ordered_elements = Vec::new();
        let mut type_map: HashMap<String, usize> = HashMap::new();
        for elem in &state.elements {
            if !type_map.contains_key(elem) {
                ordered_elements.push(elem.clone());
                type_map.insert(elem.clone(), ordered_elements.len());
            }
        }

        let mut masses = Vec::new();
        for (i, elem) in ordered_elements.iter().enumerate() {
            masses.push((i + 1, get_atomic_mass(elem), elem.clone()));
        }

        let mut atoms = Vec::new();
        for i in 0..state.num_atoms() {
            let elem = &state.elements[i];
            let type_id = type_map.get(elem).unwrap();

            let fx = state.fract_x[i];
            let fy = state.fract_y[i];
            let fz = state.fract_z[i];

            let x = lx * fx + xy * fy + xz * fz;
            let y = ly * fy + yz * fz;
            let z = lz * fz;

            atoms.push((i + 1, *type_id, [x, y, z]));
        }

        Self {
            comment: "# LAMMPS data file generated by CrystalCanvas\n# WARNING: This is a geometry template. Check 'Masses' and atom styles/coeffs.".to_string(),
            num_atoms: state.num_atoms(),
            atom_types: ordered_elements.len(),
            box_bounds: [([0.0, 0.0], "dummy".to_string()), ([0.0, 0.0], "dummy".to_string())], // Not actually used in write fn
            box_x: [0.0, lx],
            box_y: [0.0, ly],
            box_z: [0.0, lz],
            tilts: [xy, xz, yz],
            masses,
            atoms,
        }
    }

    pub fn write_to<W: Write>(&self, writer: &mut W) -> io::Result<()> {
        writeln!(writer, "{}\n", self.comment)?;
        writeln!(writer, "{} atoms", self.num_atoms)?;
        writeln!(writer, "{} atom types\n", self.atom_types)?;

        writeln!(writer, "0.0 {:.6} xlo xhi", self.box_x[1])?;
        writeln!(writer, "0.0 {:.6} ylo yhi", self.box_y[1])?;
        writeln!(writer, "0.0 {:.6} zlo zhi", self.box_z[1])?;
        writeln!(
            writer,
            "{:.6} {:.6} {:.6} xy xz yz\n",
            self.tilts[0], self.tilts[1], self.tilts[2]
        )?;

        writeln!(writer, "Masses\n")?;
        for m in &self.masses {
            writeln!(writer, "{} {:.5} # {}", m.0, m.1, m.2)?;
        }
        writeln!(writer)?;

        writeln!(writer, "Atoms # atomic\n")?;
        for a in &self.atoms {
            writeln!(
                writer,
                "{} {} {:.6} {:.6} {:.6}",
                a.0, a.1, a.2[0], a.2[1], a.2[2]
            )?;
        }
        Ok(())
    }
}

pub fn export_lammps_data(state: &CrystalState, path: &str) -> io::Result<()> {
    let mut file = File::create(path)?;
    let lammps = LammpsDataFile::from_state(state);
    lammps.write_to(&mut file)
}

