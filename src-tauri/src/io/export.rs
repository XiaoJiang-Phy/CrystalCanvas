/// Abstract structure representing a Quantum ESPRESSO input file
pub struct QeInputFile {
    pub header: String,
    pub title: String,
    pub prefix: String,
    pub ibrav: i32,
    pub num_atoms: usize,
    pub num_types: usize,
    pub cell_abc: [f64; 3],
    pub cell_cos_abc: [f64; 3],
    pub atomic_species: Vec<(String, f64)>,
    pub lattice: [[f64; 3]; 3],
    pub atoms: Vec<(String, [f64; 3])>,
    pub kpoints: [i32; 3],
}

impl QeInputFile {
    pub fn from_state(state: &CrystalState) -> Self {
        let alpha = state.cell_alpha.to_radians();
        let beta = state.cell_beta.to_radians();
        let gamma = state.cell_gamma.to_radians();
        let b = state.cell_b;
        let c = state.cell_c;

        let cx = c * beta.cos();
        let cy = c * (alpha.cos() - beta.cos() * gamma.cos()) / gamma.sin();
        let cz = (c * c - cx * cx - cy * cy).sqrt();

        let lattice = [
            [state.cell_a, 0.0, 0.0],
            [b * gamma.cos(), b * gamma.sin(), 0.0],
            [cx, cy, cz],
        ];

        let mut ordered_elements = Vec::new();
        let mut type_map: HashMap<String, usize> = HashMap::new();
        for elem in &state.elements {
            if !type_map.contains_key(elem) {
                ordered_elements.push(elem.clone());
                type_map.insert(elem.clone(), ordered_elements.len());
            }
        }

        let mut atomic_species = Vec::new();
        for elem in &ordered_elements {
            atomic_species.push((elem.clone(), get_atomic_mass(elem)));
        }

        let mut atoms = Vec::new();
        for i in 0..state.num_atoms() {
            atoms.push((
                state.elements[i].clone(),
                [state.fract_x[i], state.fract_y[i], state.fract_z[i]],
            ));
        }

        let k_density = 30.0;
        let nx = (k_density / state.cell_a).round().max(1.0) as i32;
        let ny = (k_density / state.cell_b).round().max(1.0) as i32;
        let nz = (k_density / state.cell_c).round().max(1.0) as i32;

        Self {
            header: "! Quantum ESPRESSO input generated by CrystalCanvas\n! WARNING: This is a geometry template. Verify all &CONTROL, &SYSTEM, &ELECTRONS parameters.".to_string(),
            title: format!("{} generated by CrystalCanvas", state.name),
            prefix: state.name.clone(),
            ibrav: 0,
            num_atoms: state.num_atoms(),
            num_types: ordered_elements.len(),
            cell_abc: [state.cell_a, state.cell_b, state.cell_c],
            cell_cos_abc: [gamma.cos(), beta.cos(), alpha.cos()], // cosAB, cosAC, cosBC
            atomic_species,
            lattice,
            atoms,
            kpoints: [nx, ny, nz],
        }
    }

    pub fn write_to<W: Write>(&self, writer: &mut W) -> io::Result<()> {
        writeln!(writer, "{}\n", self.header)?;

        writeln!(writer, "&CONTROL")?;
        writeln!(writer, "  title = '{}',", self.title)?;
        writeln!(
            writer,
            "  calculation = 'scf', prefix = '{}', outdir = './tmp', verbosity = 'high',",
            self.prefix
        )?;
        writeln!(
            writer,
            "  tprnfor = .true., tstress = .true., forc_conv_thr = 1.0d-7, etot_conv_thr = 1.0d-9, nstep = 100,"
        )?;
        writeln!(writer, "/\n")?;

        writeln!(writer, "&SYSTEM")?;
        writeln!(
            writer,
            "  ibrav = {}, nat = {}, ntyp = {},",
            self.ibrav, self.num_atoms, self.num_types
        )?;
        writeln!(
            writer,
            "  A = {:.10}, B = {:.10}, C = {:.10},",
            self.cell_abc[0], self.cell_abc[1], self.cell_abc[2]
        )?;
        writeln!(
            writer,
            "  cosAB = {:.10}, cosAC = {:.10}, cosBC = {:.10},",
            self.cell_cos_abc[0], self.cell_cos_abc[1], self.cell_cos_abc[2]
        )?;
        writeln!(
            writer,
            "  ecutwfc = 60, occupations = 'smearing', smearing = 'gauss', degauss = 1.0d-2,"
        )?;
        writeln!(writer, "/\n")?;

        writeln!(
            writer,
            "&ELECTRONS\n  conv_thr = 1.0d-12,\n  mixing_beta = 0.3d0,\n/\n"
        )?;
        writeln!(writer, "&IONS\n/\n")?;
        writeln!(writer, "&CELL\n  press_conv_thr = 0.02,\n/\n")?;

        writeln!(writer, "ATOMIC_SPECIES")?;
        for s in &self.atomic_species {
            writeln!(writer, "  {} {:.5} {}.upf", s.0, s.1, s.0)?;
        }
        writeln!(writer)?;

        writeln!(writer, "CELL_PARAMETERS (angstrom)")?;
        for row in &self.lattice {
            writeln!(writer, "  {:.10} {:.10} {:.10}", row[0], row[1], row[2])?;
        }
        writeln!(writer)?;

        writeln!(writer, "ATOMIC_POSITIONS (crystal)")?;
        for a in &self.atoms {
            writeln!(
                writer,
                "  {:2} {:.10} {:.10} {:.10}",
                a.0, a.1[0], a.1[1], a.1[2]
            )?;
        }
        writeln!(writer)?;

        writeln!(writer, "K_POINTS {{automatic}}")?;
        writeln!(
            writer,
            "  {} {} {} 0 0 0",
            self.kpoints[0], self.kpoints[1], self.kpoints[2]
        )?;

        Ok(())
    }
}

pub fn export_qe_input(state: &CrystalState, path: &str) -> io::Result<()> {
    let mut file = File::create(path)?;
    let qe = QeInputFile::from_state(state);
    qe.write_to(&mut file)
}
