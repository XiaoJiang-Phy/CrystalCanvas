import { readFile, writeFile } from 'node:fs/promises';
import { resolve } from 'node:path';
import { create_inventory } from './ipc-inventory.mjs';

const ROOT = resolve(import.meta.dirname, '..');
const CLASSIFICATION = resolve(ROOT, 'ipc/command-classification.json');
const OUTPUT = resolve(ROOT, 'src/ipc/commands.generated.ts');

const classification = JSON.parse(await readFile(CLASSIFICATION, 'utf8')).commands;
const commands = Object.keys(classification).sort();
const { backend_command_args } = await create_inventory();
const missing = commands.filter((command) => !Object.hasOwn(backend_command_args, command));
if (missing.length > 0) throw new Error(`Missing Rust argument schema: ${missing.join(', ')}`);

const args_type = (command) => {
    const args = backend_command_args[command];
    if (args.length === 0) return 'undefined';
    return `{ ${args.map((arg) => `${arg}: unknown`).join('; ')} }`;
};
const content = `// Generated by scripts/generate-ipc-commands.mjs. Do not edit manually.\n\nexport const IPC_COMMANDS = {\n${commands.map((command) => `    ${command}: '${command}',`).join('\n')}\n} as const;\n\nexport type IpcCommandName = typeof IPC_COMMANDS[keyof typeof IPC_COMMANDS];\n\nexport interface GeneratedIpcCommandArgs {\n${commands.map((command) => `    ${command}: ${args_type(command)};`).join('\n')}\n}\n\nexport type GeneratedIpcArgs<Command extends IpcCommandName> = GeneratedIpcCommandArgs[Command];\n`;

if (process.argv.includes('--check')) {
    const existing = await readFile(OUTPUT, 'utf8');
    if (existing !== content) {
        throw new Error('src/ipc/commands.generated.ts is stale; run npm run generate:ipc');
    }
} else {
    await writeFile(OUTPUT, content);
}
